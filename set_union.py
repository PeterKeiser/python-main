# set_union

math_students = {"Matthew", "Hellen", "Prashant", "James", "Aparna"}
print(math_students)
biology_students = {"Jane", "Matthew", "Charlotte", "Mesut", "Oliver", "James"}
print(biology_students)

union = math_students | biology_students  
print(union)

intersection = math_students & biology_students
print(intersection)